package com.demo.operators;

public class SquareRoot {

	public static void main(String[] args) {
		int var1=10;
		
		double res=Math.pow(var1, 4);
		System.out.println("Power of 10 is "+res);
		
		double root=Math.sqrt(2125.0);
		System.out.println("Square root of number is "+root);
		}

}
